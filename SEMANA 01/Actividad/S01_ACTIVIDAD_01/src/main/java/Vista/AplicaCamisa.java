    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        int cantidad;
        double precio,desc1,desc2,descTotal,impPagar,impCompra;
        
        cantidad = Integer.parseInt(txtCantidad.getText().trim());
        precio = Double.parseDouble(txtPrecio.getText().trim());
        
        if (cantidad > 0 && precio>0) {
            impCompra = cantidad * precio;
            desc1 = impCompra * .07;
            desc2 = (impCompra-desc1)*.07;
            descTotal = desc1 + desc2;
            impPagar = impCompra - descTotal;
            
            this.txtSalida.setText("\tResultados");
            this.txtSalida.append("\n\t-------------");
            this.txtSalida.append("\nImporte de compra: S/. "+String.format("%.2f", impCompra));
            this.txtSalida.append("\nPrimer descuento: S/. "+String.format("%.2f", desc1));
            this.txtSalida.append("\nSegundo descuento: S/. "+String.format("%.2f", desc2));
            this.txtSalida.append("\n\nDescuento Total: S/. "+String.format("%.2f", descTotal));
            this.txtSalida.append("\nImporte a Pagar: S/. "+String.format("%.2f", impPagar));            
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese un valor mayor a CERO");
            this.txtCantidad.setText("");
            this.txtPrecio.setText("");
            this.txtCantidad.requestFocus();
        }        
    }//GEN-LAST:event_btnCalcularActionPerformed

